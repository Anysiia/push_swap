#include "libft.h"

char	**ft_free_tab(char **tab)
{
	int	i;

	i = 0;
	while (tab && tab[i])
	{
		ft_freestr(&tab[i]);
		i++;
	}
	if (tab)
		free(tab);
	return (NULL);
}
